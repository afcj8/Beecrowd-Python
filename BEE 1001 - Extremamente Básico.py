# A função input() espera o usuário digitar algo, de acordo com o tipo específicado.

a = int(input())    # Ler o primeiro inteiro (int) e armazena o valor na variável a.
b = int(input())    # Ler o segundo inteiro (int) e armazena o valor na variável b.
x = a + b           # Realiza a soma entre as variáveis a e b e armazena o valor em x.
print("X = {:d}".format(x)) # Imprime o resultado da soma no formato inteiro {:d}.